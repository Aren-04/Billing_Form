// 2. This is your separate method OUTSIDE the button method
private void calculateTotal() {
    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
    double subtotal = 0;

    for (int i = 0; i < model.getRowCount(); i++) {
        int qty = Integer.parseInt(model.getValueAt(i, 1).toString());
        double price = Double.parseDouble(model.getValueAt(i, 2).toString());
        double total = qty * price;
        model.setValueAt(total, i, 3); // Set Total Price
        subtotal += total;
    }

    subtotalField.setText(String.valueOf(subtotal));
    finalTotalField.setText(String.valueOf(subtotal)); // No discount/tax
}
 
